package toan.tblUser;

import java.sql.Date;

/**
 *
 * @author toan
 */
public class UserDTO {

    private String name, phone, password, address, email;
    private String statusID, roleID;
    private Date createDate;

    public UserDTO(String name, String phone, String password, String address, String email, String statusID, String roleID, Date createDate) {
        this.name = name;
        this.phone = phone;
        this.password = password;
        this.address = address;
        this.email = email;
        this.statusID = statusID;
        this.roleID = roleID;
        this.createDate = createDate;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getStatusID() {
        return statusID;
    }

    public void setStatusID(String statusID) {
        this.statusID = statusID;
    }

    public String getRoleID() {
        return roleID;
    }

    public void setRoleID(String roleID) {
        this.roleID = roleID;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

}
